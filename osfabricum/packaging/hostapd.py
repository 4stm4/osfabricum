"""hostapd package builder.

Downloads hostapd source, builds with nl80211 driver (needs libnl-3-dev and
libssl-dev which are installed inside the build container at build time),
bundles all shared-library dependencies found by ``ldd`` into the ``.ofpkg``
so the initramfs has everything it needs.

Also injects network-setup, hostapd start, and udhcpd start service scripts.

Entry point::

    from osfabricum.packaging.hostapd import build_hostapd
    result = build_hostapd(arch="aarch64", store_root=Path(...), db_url=...)
"""

